package it.polito.tdp.metrodeparis.model;

public class FermataPairs {
private int f1;
private int f2;
int IdLinea;

public FermataPairs(int f1, int f2, int idLinea) {
	super();
	this.f1 = f1;
	this.f2 = f2;
	IdLinea = idLinea;
}

public int getF1() {
	return f1;
}
public void setF1(int f1) {
	this.f1 = f1;
}
public int getF2() {
	return f2;
}
public void setF2(int f2) {
	this.f2 = f2;
}
public int getIdLinea() {
	return IdLinea;
}
public void setIdLinea(int idLinea) {
	IdLinea = idLinea;
}







}

